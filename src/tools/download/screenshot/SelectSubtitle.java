  private static class SubtitleListRenderer extends JLabel implements ListCellRenderer {

    public SubtitleListRenderer() {
      setOpaque(true);
      setBorder(BorderFactory.createEmptyBorder());
      setHorizontalAlignment(LEFT);
      setPreferredSize(new Dimension(this.getWidth(), 20));
    }

    public Component getListCellRendererComponent(JList list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
      if (isSelected) {
        setBorder(BorderFactory.createLineBorder(Color.BLACK));
        setFont(getFont().deriveFont(Font.BOLD));
      } else {
        setBorder(BorderFactory.createEmptyBorder(1,1,1,1));
        setFont(getFont().deriveFont(Font.PLAIN));
      }
      if (value instanceof Subtitle) {
        Subtitle sub = (Subtitle) value;
        setText(sub.toString());
        setIcon(sub.getIcon());
        setBackground(sub.getBgColor());
      } else {
        setText(value.toString());
      }

      return this;
    }
  }
